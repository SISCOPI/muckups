package pruebas;

/**
 *
 * @author maricela
 */
public class Horario {
    
    private int dia;
    private String horaDe;
    private String horaA;

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public String getHoraDe() {
        return horaDe;
    }

    public void setHoraDe(String horaDe) {
        this.horaDe = horaDe;
    }

    public String getHoraA() {
        return horaA;
    }

    public void setHoraA(String horaA) {
        this.horaA = horaA;
    }
    
}
